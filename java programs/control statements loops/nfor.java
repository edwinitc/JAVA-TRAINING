public class nfor {
    public static void main(String args[]){
        int i = 0;
        int j = 1;
        int a = 9;
        int b = 9 ;
        for(i=0;i<=a;++i)
        {
            System.out.println("number ="+i);
            for(j=1;j<=b;++j)
            {
                System.out.println(""+i+"."+j);
            }
        }
    }
}
